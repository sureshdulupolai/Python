# Tuples:-

print()
# --------------------------------------------------------
a = ()                      # empty tuple
b = (10,)                   # tuple with a single element
c = ('Suresh', 18, 70000)   # tuple with dissimilar elements
d = (10, 20, 30, 40, 50)    # tuple with similar elements

print()
# --------------------------------------------------------
c = 'suresh', 23, 7000      # round brackets are present by default 
print(c)                    # indicating this as a tuple

print()
# ---------------------------------------------------------
d = (10) * 5                # this is 10 * 5 = 50
print(d)

e = (10, ) * 5              # this will result is 5 times 10
print(e)                    # (10, 10, 10, 10, 10)
